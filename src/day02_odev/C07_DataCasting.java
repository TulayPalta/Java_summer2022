package day02_odev;

public class C07_DataCasting {
    public static void main(String[] args) {
        byte sayi1=23;
        short sayi2=12;
        long sayi3=179;
        float sayi4=3.5f;
        int sayi5=997;
        double sayi6=15.8;
        System.out.println("sayi1+sayi2  :"+(sayi1+sayi2));
        System.out.println("sayi3*sayi4 :"+(sayi3*sayi4));
        System.out.println("sayi6-sayi5 :"+(sayi6-sayi5));
        System.out.println("sayi6/sayi4  :"+(sayi6/sayi4));
    }
}
